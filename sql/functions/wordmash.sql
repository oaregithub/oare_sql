CREATE DEFINER=`oare`@`%` FUNCTION `wordmash`(word VARCHAR(56)) RETURNS varchar(56) CHARSET utf8mb4 COLLATE utf8mb4_bin
BEGIN 
	DECLARE mash VARCHAR(56);
    SET mash = REPLACE(word, "-", "");
    SET mash = REPLACE(mash, "(f)","");
    SET mash = REPLACE(mash, "(?)","");
    SET mash = REPLACE(mash, ".","");
    SET mash = REPLACE(mash, "(","");
    SET mash = REPLACE(mash, ")","");
    SET mash = REPLACE(mash, "₀","");
	  SET mash = REPLACE(mash, "₁","");
	  SET mash = REPLACE(mash, "₂","");
    SET mash = REPLACE(mash, "₃","");
    SET mash = REPLACE(mash, "₄","");
    SET mash = REPLACE(mash, "₅","");
    SET mash = REPLACE(mash, "₆","");
    SET mash = REPLACE(mash, "₇","");
    SET mash = REPLACE(mash, "₈","");
    SET mash = REPLACE(mash, "ₓ","");
    SET mash = REPLACE(mash, "á","A");
    SET mash = REPLACE(mash, "à","A");
    SET mash = REPLACE(mash, "ā","A");
    SET mash = REPLACE(mash, "é","E");
    SET mash = REPLACE(mash, "è","E");
    SET mash = REPLACE(mash, "ē","E");
    SET mash = REPLACE(mash, "ì","I");
    SET mash = REPLACE(mash, "ī","I");
    SET mash = REPLACE(mash, "í","I");
    SET mash = REPLACE(mash, "ú","U");
    SET mash = REPLACE(mash, "ù","U");
    SET mash = REPLACE(mash, "ū","U");
    SET mash = REPLACE(mash, "ṣ","S");
    SET mash = REPLACE(mash, "z","S");
    SET mash = REPLACE(mash, "ṭ","D");
    SET mash = REPLACE(mash, "t","D");
    SET mash = REPLACE(mash, "p","B");
    SET mash = REPLACE(mash, "g","G");
    SET mash = REPLACE(mash, "k","G");
    SET mash = REPLACE(mash, "q","G");
    SET mash = REPLACE(mash, "w","");
    SET mash = UPPER(mash);
    SET mash = TRIM(mash);
    SET mash = REPLACE(mash, "Ṣ", "S");
    SET mash = REPLACE(mash, "Z", "S");
    SET mash = REPLACE(mash, "T", "D");
    SET mash = REPLACE(mash, "Ṭ", "D");
    SET mash = REPLACE(mash, "K", "G");
    SET mash = REPLACE(mash, "Q", "G");
    SET mash = REPLACE(mash, "P", "B");
    SET mash = REPLACE(mash, "W", "");
    SET mash = REPLACE(mash, "AAA", "A");
    SET mash = REPLACE(mash, "AA", "A");
    SET mash = REPLACE(mash, "EEE", "E");
    SET mash = REPLACE(mash, "EE", "E");
    SET mash = REPLACE(mash, "III", "I");
    SET mash = REPLACE(mash, "II", "I");
    SET mash = REPLACE(mash, "UUU", "U");
    SET mash = REPLACE(mash, "UU", "U");
    SET mash = REPLACE(mash, "BB", "B");
    SET mash = REPLACE(mash, "DDD", "D");
    SET mash = REPLACE(mash, "DD", "D");
    SET mash = REPLACE(mash, "BBB", "B");
    SET mash = REPLACE(mash, "GGG", "G");
    SET mash = REPLACE(mash, "GG", "G");
    SET mash = REPLACE(mash, "ḪḪḪ", "Ḫ");
    SET mash = REPLACE(mash, "ḪḪ", "Ḫ");
    SET mash = REPLACE(mash, "LLL", "L");
    SET mash = REPLACE(mash, "LL", "L");
    SET mash = REPLACE(mash, "MMM", "M");
    SET mash = REPLACE(mash, "MM", "M");
    SET mash = REPLACE(mash, "NNN", "N");
    SET mash = REPLACE(mash, "NN", "N");
    SET mash = REPLACE(mash, "RRR", "R");
    SET mash = REPLACE(mash, "RR", "R");
    SET mash = REPLACE(mash, "SSS", "S");
    SET mash = REPLACE(mash, "SS", "S");
    SET mash = REPLACE(mash, "ŠŠŠ", "Š");
    SET mash = REPLACE(mash, "ŠŠ", "Š");
    IF (mash REGEXP ' ') THEN
		  SET mash = LEFT(mash, POSITION(' ' IN mash) - 1);
	  END IF;
    RETURN mash;

END